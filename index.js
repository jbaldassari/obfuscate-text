const SUBSTITUTION_PROBABILITY = 0.5;

function obfuscateCharacter(character) {
  const characterSet = EQUIVALENCE.find((alike) => alike.has(character));
  if (characterSet) {
    if (Math.random() <= SUBSTITUTION_PROBABILITY) {
      const characters = Array.from(characterSet.values());
      return characters[Math.floor(Math.random() * characters.length)];
    }
  }
  return character;
}

function obfuscate(input) {
  return Array.from(input.toString()).map(obfuscateCharacter).join("");
}

